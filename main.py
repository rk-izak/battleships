letter_rows = {'A':0, 'B':1, 'C':2, 'D':3, 'E':4, 'F':5, 'G':6, 'H':7, 'I':8, 'J':9}
number_rows = {'1 ':0, '2 ':1, '3 ':2, '4 ':3, '5 ':4, '6 ':5, '7 ':6, '8 ':7, '9 ':8, '10':9}
print('    A', '  B', '  C', '  D', '  E', '  F', '  G', '  H', '  I', '  J')
